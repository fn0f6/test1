
-- ==========================================================
-- 1. إنشاء الجداول الأساسية
-- ==========================================================

-- جدول الإعدادات (Settings)
CREATE TABLE IF NOT EXISTS public.settings (
    id INT PRIMARY KEY DEFAULT 1,
    logo_url TEXT,
    hero_bg_url TEXT,
    site_title TEXT DEFAULT 'عصر الهامور',
    android_url TEXT DEFAULT '#',
    ios_url TEXT DEFAULT '#',
    is_maintenance_mode BOOLEAN DEFAULT FALSE,
    maintenance_message TEXT DEFAULT 'الموقع تحت الصيانة حالياً، سنعود قريباً!',
    showcase_images JSONB DEFAULT '{}'::jsonb,
    translations JSONB DEFAULT '{}'::jsonb,
    social_links JSONB DEFAULT '{}'::jsonb,
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- جدول ملفات المستخدمين (Profiles)
CREATE TABLE IF NOT EXISTS public.profiles (
    id UUID REFERENCES auth.users ON DELETE CASCADE NOT NULL PRIMARY KEY,
    email TEXT,
    role TEXT DEFAULT 'user', -- 'admin' or 'user'
    display_name TEXT,
    avatar_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- جدول الأخبار (News)
CREATE TABLE IF NOT EXISTS public.news (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    title TEXT NOT NULL,
    excerpt TEXT,
    thumbnail_url TEXT,
    category TEXT DEFAULT 'تحديث',
    date TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- جدول رسائل الدعم (Tickets)
CREATE TABLE IF NOT EXISTS public.tickets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT,
    email TEXT,
    subject TEXT,
    message TEXT,
    is_read BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

